import turtle
import random
import time

turtle.bgcolor('black')
turtle.hideturtle()
turtle.speed(0)
turtle.delay(0)

turtle.penup()
turtle.goto(320, 180)
turtle.pendown()
turtle.pencolor("white")
turtle.write("游戏说明：\n控制键：\n↑ 打上面地鼠\n↓ 打下面地鼠\n← 打左面地鼠\n→ 打右面地鼠\n", align="right", font=("楷体", 14, "normal"))
turtle.pencolor("black")


def chuizi():
    turtle.hideturtle()
    turtle.begin_fill()
    turtle.fillcolor("#804000")
    turtle.left(135)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(30)
    turtle.right(90)
    turtle.forward(30)
    turtle.right(90)
    turtle.forward(65)
    turtle.right(90)
    turtle.forward(30)
    turtle.right(90)
    turtle.forward(30)
    turtle.left(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(5)
    turtle.end_fill()
    turtle.penup()
    turtle.home()


