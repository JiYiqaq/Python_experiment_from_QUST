def shang():
    turtle.penup()
    turtle.goto(100, 200)
    chuizi()
    time.sleep(0.5)
    turtle.pendown()
    turtle.pencolor('black')
    turtle.color('black')
    turtle.begin_fill()
    turtle.circle(150)
    turtle.end_fill()
    turtle.penup()
    turtle.home()


