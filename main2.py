def huayuan():
    colors = random.choice(["pink", "yellow", "white", "red", "green"])
    x = random.choice([0, -200, 200])
    if x == 0:
        y = random.choice([-200, 200])
    else:
        y = 0
    turtle.penup()
    turtle.goto(x, y)
    turtle.pendown()
